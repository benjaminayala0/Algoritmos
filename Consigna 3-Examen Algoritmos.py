#A
def listado_nombre(jedis):
    return sorted(jedis, key=lambda x: x['name'])

def listado_especie(jedis):
    return sorted(jedis, key=lambda x: x['species'] if x['species'] is not None else '')

def imprimir_listado(jedis, criterio):
    if criterio == 'nombre':
        jedis_ordenados = listado_nombre(jedis)
    elif criterio == 'especie':
        jedis_ordenados = listado_especie(jedis)
    else:
        return

    print(f"Listado ordenado por {criterio}:")
    for jedi in jedis_ordenados:
        print(f"nombre: {jedi['name']}, especie: {jedi['species']}")

imprimir_listado(jedis, 'nombre')
imprimir_listado(jedis, 'especie')


#B
def info_jedi(jedis, nombres):
    for jedi in jedis:
        if jedi['name'] in nombres:
            print(f" {jedi['name']}:")
            for key, value in jedi.items():
                print(f"{key}: {value}")

nombres_jedis = ["Ahsoka Tano", "Kit Fisto"]
info_jedi(jedis, nombres_jedis)

#C
def padawans(jedis, maestro):
    print(f"Padawans de {maestro}:")
    for jedi in jedis:
        if jedi['master'] and maestro in jedi['master']:
            print(f" {jedi['name']}:")
            for key, value in jedi.items():
                print(f"{key}: {value}")

padawans(jedis, "Yoda")
padawans(jedis, "Luke Skywalker")


